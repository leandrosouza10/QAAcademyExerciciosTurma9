package metodoSemRetorno;

/*
    1- Criar uma classe;
    2- Criar 5 metodos sem retorno:
    3- Adicionar uma texto no console(System.out) para exibir o nome do Método;
    4- Fazer a chamada destes 5 métodos em um main.
*/


public class MetodoSemRetorno {

    public static void metodoUm() {
        System.out.println("MÉTODO UM");
    }

    public static void metodoDois() {
        System.out.println("MÉTODO DOIS");
    }

    public static void metodoTres() {
        System.out.println("MÉTODO TRÊS");
    }

    public static void metodoQuatro() {
        System.out.println("MÉTODO QUATRO");
    }

    public static void metodoCinco() {
        System.out.println("MÉTODO CINCO");
    }

    public static void main(String[] args) {
        metodoUm();
        metodoDois();
        metodoTres();
        metodoQuatro();
        metodoCinco();
    }

}
